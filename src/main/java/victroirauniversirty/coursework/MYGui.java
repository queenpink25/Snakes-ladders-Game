    private void StartStopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartStopActionPerformed
      var currentInstance = this.StartStop.getText();
      if(currentInstance=="StartGame"){
          this.StartStop.setBackground(Color.red);
          this.DiceRoller.setEnabled(true);
           this.StartStop.setForeground(Color.white);
          this.StartStop.setText("StopGame");
        
         //var res =  this.bLogic.boxpopulator(this.AnchoringLabel.getSize());
        
      }
      else{
           this.StartStop.setBackground(Color.white);
          this.StartStop.setText("StartGame");
           this.StartStop.setForeground(Color.black);
            this.DiceRoller.setEnabled(false);
      }
    }//GEN-LAST:event_StartStopActionPerformed

